    <h1 class="centre">MOT DU DIRECTEUR</h1>
    <p>Depuis son ouverture en septembre 2010, félix moumié , s’efforce d’être une école qui enseigne
        et qui éduque en complément del’éducation reçue à la maison. L’excellence éducative pour tous
        est l’objectif fondamental mis en œuvre à l’école, en accord avec le programme français.Chaque année,
        nous relevons le défi d’assurer à chaque élève la réalisation de son propre parcours d’excellence.
        C’est ensemble, élèves, parents et personnel de cet établissement que nous y parvenons.
        Notre projet d’école vise à assurer les connaissances et les compétences  que doit maîtriser l’homme
        et le citoyen du XXIème siècle, pour son épanouissement personnel et sa performance professionnelle.
    </p></br>
    <p class="centre"><strong>SIX AXES SONT DECLINES DANS NOS PROJETS D'ECOLE.</strong></p>
    <ul>
        <li>Développer le parcours des élèves autour de la réussite de tous nos élèves grâce à la mise en place d’une scolarité performante,développant le sens de l’effort, de la solidarité et assurant une meilleure valorisation des capacités.L’école met en place des aides personnalisées pour les élèves qui en ont  besoin.</li>

        <li>Rechercher une qualité d’enseignement optimisée dans chaque domaine en mobilisant tous les acteurs de l’équipe pédagogique de l’école.Pour cela, encourager une évolution harmonisée des pratiques d’enseignement et d’évaluation.
        </li>
        <li>Favoriser et encourager, dès la maternelle, la pratique des langues étrangères. Cet enseignement des langues a vocation à assurer l’ouverture et la diversité culturelles, la capacité à vivre ensemble.</li>
        <li>Renforcer l’éducation aux arts qui enrichit la sensibilité de chacun de nos élèves et les initie aux réalisations artistiques les plus variées.</li>
        <li>Développer l’éducation aux sciences en favorisant la démarche d’investigation et d’expérimentation, en développant l’esprit critique, l’innovation et l’action sur l’environnement.</li>
        <li>Assurer les valeurs civiques et morales en développant l’esprit citoyen concerné par l’environnement, prêts à faire face aux défis complexes du monde moderne et à jouer ainsi un rôle actif dans la construction du monde de demain.</li>
    </ul>
    <p>J’ai à cœur<strong> l’accomplissement</strong> de cette mission auprès des 1 100 élèves inscrits cette année.
        Je vous <strong>assure</strong> de ma volonté et de mon <strong>engagement</strong> au service de vos enfants.
        Toute une équipe de cadres compétents et dévoués travaille en collaboration avec vous et moi au sein de notre établissement.
        Ce sont là tous les fondements de la réussite de notre mission !</p></br>

    <p>
        Ce site est en développement mais il se veut déjà énergique et accessible, tout à l’image de notre milieu de vie. Nous sommes à votre écoute pour toutes suggestions visant à le rendre encore plus convivial et répondant à vos attentes.
        Bonne visite !
    </p>
