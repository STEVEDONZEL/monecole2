<section><h1>Règlement intérieur primaire</br></h1>
    <h2>Préambule</h2>
    <p>L’Ecole a pour but :</p>
    <ul>
        <li>de donner aux élèves des connaissances et des habitudes de travail</li>
        <li>de favoriser leur épanouissement et de leur apprendre à vivre au sein d’une collectivité</li>
    </ul>
    <h2>1) ENTREES ET SORTIES DE L’ETABLISSEMENT</br></h2>
    <p>Elles se font uniquement par le grand portail   « garage à vélos » est situé à côté du portail.<p></p>
    Je dépose obligatoirement mon vélo dans le « garage à vélos » et je le cadenasse.</br></p>

    <h2>2) TRAVAIL SCOLAIRE</h2>
    <p>Je note sérieusement dans l’agenda le travail et les leçons donnés par l’enseignant</p>
    <p>Je m’engage à faire mes devoirs et mes leçons pour le jour demandé</p>
    <p>Je dois être en possession de mon matériel scolaire et sportif tout au long de l’année</p>
    <p>Je travaille sérieusement et avec application en classe comme en étude</br></p>

    <h2>3) RECREATIONS</h2>
    <p>Dans la cour, je n’utilise que les ballons de l’Etablissement, aux endroits et aux périodes réservés à cet effet (consignes affichées en salle de classe).</p>
    <p>Je m’engage à ne pas ramener dans l’Etablissement jeux électroniques, baladeurs, téléphones portables et tout objet entraînant échanges et
        risques de racket ainsi que tout objet dangereux. Si besoin, je demande l’autorisation à l’enseignant.</p>
    <p>Aux sonneries, je me range sans délai.</p>

    <h2>4) RESPECT DES PERSONNES, MATERIEL, ENVIRONNEMENT ET SECURITE</h2>
    <h2>a) Respect des personnes</h2>
    <p>Je m’engage à avoir un comportement citoyen en respectant les règles de politesse, en refusant toute forme de violence (physique, morale, verbale).</p>
    <p>En cas de problème concernant l’un de mes camarades ou moi-même, je m’adresse à tout adulte – membre de la communauté éducative – auquel il appartient de gérer les conflits.</p>
    <p>Je viens à l’école avec une tenue décente, j’enlève mon couvre-chef à l’entrée du bâtiment et je m’engage à ne pas véhiculer par des signes ostentatoires toute idée incitant à la haine, à l’intolérance et à tout ce qui peut porter préjudice à autrui.</p>
    <p>Je ne perturbe pas mes camarades par mon chahut ou ma mauvaise conduite.</p>

    <h2>b) Respect des biens</h2>
    <p>Chacun est responsable de ses affaires et de son matériel.L’Etablissement décline toute responsabilité en cas de vol,
        perte ou dégradation. Les vêtements trouvés seront déposés dans les caisses prévues à cet effet (cf. affichage) ; en fin d’année, les vêtements non réclamés seront donnés à des œuvres caritatives.</p>
    <p>Pour travailler dans de bonnes conditions, je m’engage à respecter le matériel mis à ma disposition (mobilier, manuels scolaires, livres de bibliothèque). Toute dégradation entraînera un dédommagement par les familles.</p>
    <h2>c) Respect de l’environnement</h2>
    Afin de préserver la qualité de mon cadre de vie (bâtiments, espaces verts, bancs, buts de foot), je ne traverse pas les espaces verts, je ne crache pas et ne jette aucun déchet par terre.
    <p>Je sais que le chewing-gum est interdit à l’intérieur du bâtiment.</p>
    <p>Au self, je ne joue pas avec la nourriture, me tiens correctement à table et je respecte la file d’attente.</p>
    <h2>d) Respect des consignes de sécurité</h2>
    <p>Aucune personne de l’Etablissement n’est autorisée ou habilitée à administrer des médicaments . </p>
    <p>Aucun médicament ne sera laissé dans la poche ou dans le sac de l’enfant. Dans des cadres spécifiques (diabète, allergie grave) un PAI (Projet d’Accueil Individualisé) est mis en place : les médicaments sont alors stockés dans un endroit connu de tous les adultes concernés.</p>
    <p>En cas d’accident, l’équipe enseignante fait appel aux services d’urgence et la famille est prévenue immédiatement.</p>
    <p>Les enfants accueillis à l’école doivent être dans un état de santé et de propreté compatibles avec les exigences de la scolarisation.</p>
    <p>Pour des raisons de santé, le piercing est interdit.</br></p>
    <h2>5) ABSENCES </h2>
    <p>Toute absence doit être excusée :</p>
    <ul> <li>à l’avance (en cas d’absence prévue)</li>
        <li>le matin même du jour de l’absence (en cas d’absence imprévue) A son retour, l’élève doit présenter à l’enseignant une excuse rédigée par les parents (avec le motif de l’absence).</br></li></ul>
    <h2>6) DROIT A L’IMAGE</h2>
    <p>L’Institut de l’Assomption utilise les photos des élèves prises dans le cadre d’activités scolaires pour constituer des photos de classe, des exposés,
        des articles dans la presse et sur le site de l’école www.assomptioncolmar.fr</br></p>

    <h2>7) SANCTIONS</h2>
    <p>Le non-respect de ce règlement entraîne les sanctions suivantes :</p>
    <ul><li>Avertissement oral ou écrit</li>
        <li> Carton jaune dans la cour (3 cartons = 1 sanction)</li>
        <li>Travail supplémentaire</li>
        <li> Retenue le mercredi</li>
        <li>Convocation des parents (avec l’enseignant et la direction)</li>
        <li> Exclusion temporaire </li>
        <li>Exclusion des études, de la cantine, des activités FERIA</li>
        <li>Non réinscription en cas de différend important avec la famille ou de problème grave avec l’enfant
            Tout le personnel éducatif de l’Etablissement est habilité à sanctionner. </li></ul>
