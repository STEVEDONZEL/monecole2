    <h1>Services aux élèves</h1>
    <h2>Voici les divers services offerts aux élèves.</h2></br>
    <ul>
        <li>
         <strong> Orthopédagogie</strong>   : s’adresse à l’élève ayant des difficultés, afin de le soutenir dans l’apprentissage des disciplines de base (français, mathématique).
        </li></br/
        <li>
          <strong>Soutien linguistique </strong>  : s’adresse à l’élève inscrit en classe ordinaire et ayant bénéficié de services d’accueil.
        </li>
        <li>
            <strong> Psychologie</strong>  : vise à amener l’élève à se prendre en charge en tant que personne ayant une vie sociale,
            affective et intellectuelle saine, dans les conditions les plus favorables à une croissance équilibrée.
        </li>
        <li>
            <strong> Psychoéducation </strong> : vise à favoriser l’adaptation socio-affective et l’autonomie fonctionnelle de l’élève dans les conditions les plus favorables au développement de comportements adaptés.
            Programme en vue de contrer l’intimidation.
        </li>
        <li>
            <strong> Orthophonie</strong> : vise à développer chez l’élève sa communication orale, principalement en intervenant dans les difficultés relatives à la parole,
            au langage oral et écrit et à l’audition et ce, dans les conditions les plus favorables au développement langagier.
        </li>
        <li>
            <strong>Animation à la vie spirituelle et à l’engagement communautaire (AVSEC)</strong>   : vise à favoriser chez l’élève le développement d’une vie spirituelle autonome et
            responsable et sa contribution à l’édification d’une société harmonieuse et solidaire dans le respect des droits et libertés de conscience et de religion.
        </li>
        <li>
            Le technicien ou la technicienne en éducation spécialisée (TES) est un spécialiste qui apporte une aide appropriée à
            l’élève et à sa famille qui fait face à des difficultés d’ordre social, d’ordre émotif ou de comportement.
        </li>
        <li>
            <strong> Ateliers d’habiletés sociales</strong> : sont habituellement définies comme étant la capacité de démontrer un comportement approprié
            dans une variété de contextes comme à la maison, à l’école ou au travail et dans la société en général.
        </li>
        <li>
            <strong> Psychomotricité </strong> : vise à privilégier la prise de conscience de soi au niveau global (corporel, émotionnel, physique et cognitif) et de l’environnement spatio-temporel.
        </li>
        <li>
            <strong> Aide aux devoirs</strong>  : s’adresse à l’élève dans le but qu’il fasse ses devoirs avec une personne qualifiée dans un endroit calme et sécuritaire (groupes de 8 élèves environ).
        </li>
        <li>
            <strong> Activités extrascolaires</strong>  : organisées par l’école pour un prix modique en collaboration avec Éduc Action et offertes à l’élève, afin qu’il développe ses habiletés dans un domaine en particulier
        </li>
        <li>
            <strong>Service de garde</strong>   : est offert aux élèves de l’école durant l’année scolaire, soit lors des jours de classe, soit lors de certaines journées pédagogiques.
            Ce service est disponible en dehors des heures d’enseignement, soit avant (dès 7 h) et après les heures de classe (jusqu’à 18 h) et pendant l’heure du dîner.
        </li>
        <li>
            <strong>Service de dîner</strong>  : est offert aux élèves de l’école. À cela s’ajoute un programme spécial de repas chauds qui est offert aux familles à faible revenu
        </li>
    </ul>
