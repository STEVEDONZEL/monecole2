Explorer le monde
Le fonctionnement de l'école maternelle
Une école maternelle comprend uniquement des classes de maternelle, de la petite à la grande section. Une école primaire comprend à la fois des classes de maternelle et des classes d'élémentaire, de la petite section au CM2.
Les enfants de moins de 3 ans peuvent être scolarisés dans des modalités d'accueil différentes :
dans une classe de l'école maternelle, spécifique et adaptée aux besoins des jeunes enfants
dans des classes de l'école maternelle comportant un ou plusieurs niveaux
dans un milieu mixte, associant services de petite enfance et école, permettant d'offrir du temps scolaire dans des dispositifs conçus localement
Le programme d'enseignement
Il est organisé en cinq domaines d'apprentissage :
Mobiliser le langage dans toutes ses dimensions
Agir, s'exprimer, comprendre à travers l'activité physique
Agir, s'exprimer, comprendre à travers les activités artistiques
